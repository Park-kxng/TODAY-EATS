- 투데이츠는 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스 제공에 관한 책임이 면제됩니다.
- 투데이츠는 사용자의 귀책사유로 인한 서비스 이용 장애에 대하여 책임을 지지 않습니다.

5. 분쟁 해결
- 본 약관 및 서비스 이용과 관련하여 분쟁이 발생할 경우, 투데이츠와 사용자는 분쟁을 원만하게 해결하기 위해 노력합니다.

