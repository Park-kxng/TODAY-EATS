- 본 앱은 맞춤형 맛집 추천을 위해 사용자의 실시간 위치 정보를 사용합니다.
- 사용자는 위치 정보 사용에 동의함으로써 맞춤형 서비스를 받을 수 있으며, 언제든지 위치 서비스의 사용을 중지할 수 있습니다.
- 위치 정보는 맛집 추천 및 공유 외의 목적으로 사용되지 않습니다.

3. 커뮤니티 이용 규칙
- 사용자는 커뮤니티 내에서 자신이 방문한 맛집에 대한 리뷰와 평가를 공유할 수 있습니다.
- 공유된 맛집 정보는 해당 지역의 동/읍/면에 있는 사용자들에게만 보여집니다.
- 부적절한 언어 사용, 개인정보 공유, 허위 정보 게시 등은 금지되어 있으며, 이를 위반할 경우 서비스 이용에 제한을 받을 수 있습니다.

