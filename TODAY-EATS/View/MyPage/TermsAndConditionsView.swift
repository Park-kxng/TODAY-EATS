- 투데이츠는 사용자의 위치 정보를 활용하여 주변의 맛집을 추천하고, 사용자가 직접 경험한 맛집 정보를 공유할 수 있는 플랫폼을 제공합니다.
- 본 서비스를 통해 동/읍/면 단위의 커뮤니티 내에서 맛집 정보가 활발하게 공유됩니다.

